package com.study.Operator4;

import java.util.Scanner;

public class Count_4
{
    //算数操作符
    //基本的有：+ - * / %
    //自增 自减： ++ --

    //> 大于
    //>= 大于或等于
    //< 小于
    //<= 小于或等于
    //== 是否相等
    //!= 是否不等

    public static void main(String[] args)
    {
        int a = 5;
        int b = 6;
        int c = 5;

        System.out.println(a>b); //返回False
        System.out.println(a>=c);  //返回 true
        System.out.println(a==b); //返回false
        System.out.println(a!=b);//返回true



    }
}

