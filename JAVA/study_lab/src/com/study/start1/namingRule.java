package com.study.start1;

public class namingRule
{
    //变量命名只能使用字母 数字 $ _
    //变量第一个字符 只能使用 字母 $ _
    //变量第一个字符 不能使用数字
    int a = 5;
    int a_12 = 5;
    int $a43 = 5;
    int a434 = 5;

    //第一个字符不能是数字
    // int 34a = 5;

    //在命名的时候，尽量使用完整的单词进行命名，比如name,moveSpeed，而不是使用缩写 n,m。

    //不能只使用关键字，但是可以包含关键字
    int class5 = 5;

    //练习
    int a_;
    //int a@;
    int a3;
    //int 8@;
    //int 9_;
    int X$_;
    int y;
    int _$_;
    int $_$;
    //int $*$;
    int $1$;
    int _1_;
    //int _@_;
    //int a#;
    //int a"; +
    //int 123a";
    //int 123a_;
    int $123b_;



}
